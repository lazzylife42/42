#include "ft_strrev.h"

char	*my_ft_strrev(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z') || (str[i] >= 'a' && str[i] <= 'z'))
			str[i] = (str[i] % 26) + str[i];
		i++;
	}
	return (str);
}
